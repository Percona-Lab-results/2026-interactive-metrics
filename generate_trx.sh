#!/bin/bash
# Generates Finch trx SQL files and stage.yaml for sysbench oltp_read_write.
# Usage: ./generate-trx.sh [num_tables] [table_size] [clients] [runtime]
# Defaults: 16 clients, 20 tables, 10000 rows, 300s

CLIENTS=${1:-16}
TABLES=${2:-20}
TABLE_SIZE=${3:-5000000}
RUNTIME=${4:-900s}

TRX_DIR="trx"
STAGE_FILE="oltp_read_write.yaml"

mkdir -p "$TRX_DIR"

# Generate one SQL file per table
for i in $(seq 1 "$TABLES"); do
  cat > "$TRX_DIR/oltp-rw-sbtest${i}.sql" << EOF

BEGIN

SELECT c FROM sbtest${i} WHERE id = @id

SELECT c FROM sbtest${i} WHERE id BETWEEN @id AND @id + 99

SELECT SUM(k) FROM sbtest${i} WHERE id BETWEEN @id AND @id + 99

SELECT c FROM sbtest${i} WHERE id BETWEEN @id AND @id + 99 ORDER BY c

SELECT DISTINCT c FROM sbtest${i} WHERE id BETWEEN @id AND @id + 99 ORDER BY c

UPDATE sbtest${i} SET k = k + 1 WHERE id = @id

UPDATE sbtest${i} SET c = @c WHERE id = @id

DELETE FROM sbtest${i} WHERE id = @id

INSERT INTO sbtest${i} (id, k, c, pad) VALUES (@id, @k, @c, @pad)

COMMIT
EOF
done

echo "Generated $TABLES SQL files in $TRX_DIR/"

# Generate stage.yaml
cat > "$STAGE_FILE" << EOF
# Finch Stage: oltp_read_write (sysbench equivalent)
# Generated by generate-trx.sh: $TABLES tables, $TABLE_SIZE rows/table, $CLIENTS clients, $RUNTIME runtime
#
# Run:
#   finch stage.yaml

stage:
  runtime: $RUNTIME

  mysql:
    username: "root"
    password: "password"
    db: "sbtest"

  trx:
EOF

# Append one trx entry per table
for i in $(seq 1 "$TABLES"); do
  cat >> "$STAGE_FILE" << EOF
    - name: oltp-rw-sbtest${i}
      file: trx/oltp-rw-sbtest${i}.sql
      data:
        id:
          generator: "int"
          scope: trx
          params:
            min: 1
            max: $TABLE_SIZE
        k:
          generator: "int"
          scope: trx
          params:
            min: 1
            max: 5000000
        c:
          generator: "str-fill-az"
          scope: trx
          params:
            len: 120
        pad:
          generator: "str-fill-az"
          scope: trx
          params:
            len: 60
EOF
done

# Build trx reference list and append workload
TRX_LIST=$(seq 1 "$TABLES" | awk '{printf "oltp-rw-sbtest%d", $1; if ($1 < '"$TABLES"') printf ", "}')

cat >> "$STAGE_FILE" << EOF

  workload:
    - clients: $CLIENTS
      trx: [$TRX_LIST]
EOF

echo "Generated $STAGE_FILE"